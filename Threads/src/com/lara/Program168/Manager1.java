package com.lara.Program168;

public class Manager1 {

	public static void main(String[] args) {
		for (int i = 0; i < 1000; i++) {

			System.out.println(i);
		}
		for (int i = 1000; i < 2000; i++) {

			System.out.println(i);
		}
	}/*
	Here we are getting sequential output from top to bottom.First execution of
	first for loop for 1000 times then 1000 iteration for second for loop*/ 
}
